if(process.env.NODE_ENV != "production"){
   require("dotenv").config();
}

const express = require("express");
const app = express();
const path = require("path");
const mongoose = require("mongoose");
const methodOverride = require("method-override");
const ejsMate = require("ejs-mate");
const session = require("express-session");
const MongoStore = require('connect-mongo');
const flash = require("connect-flash");
const passport = require("passport");
const LocalStrategy = require("passport-local");
const User = require("./models/user.js");

const listingRouter = require("./routes/listing.js");
const reviewRouter = require("./routes/review.js");
const userRouter = require("./routes/user.js");

const dburl = process.env.ATLASDB_URL;

main()
.then((res)=>{
    console.log("connected to MongoDB");
})
.catch((err)=>{
    console.log(err);
});

async function main() {
    await mongoose.connect(dburl);
};

app.set("view engine","ejs");
app.set("views",path.join(__dirname,"views"));
app.use(express.static(path.join(__dirname,"/public")));
app.use(express.urlencoded({extended : true}));
app.use(methodOverride("_method"));
app.engine("ejs",ejsMate);

const store = MongoStore.create({
    mongoUrl: dburl,
    crypto : {
        secret: process.env.SECRET,
    },
    touchAfter: 24 * 3600,
});

store.on("error",()=>{
    console.log("error in mongo session  store",err);
})
const sessionOptions = {
    store,
    secret :process.env.SECRET,
    resave : false,
    saveUninitialized : true,
    cookie : {
        expires: Date.now() + 7 * 24 * 60 * 1000,
        maxAge:  + 7 * 24 * 60 * 1000,
        httpOnly : true,
    }
};


app.use(session(sessionOptions));
app.use(flash());

app.use(passport.initialize());
app.use(passport.session());
passport.use(new LocalStrategy(User.authenticate()));

passport.serializeUser(User.serializeUser());
passport.deserializeUser(User.deserializeUser());

app.use((req,res,next)=>{
  res.locals.success = req.flash("success");
  res.locals.error = req.flash("error");
  res.locals.currUser = req.user;
  next();
})

app.use((req, res, next) => {
    res.locals.currUser = req.user; // req.user comes from passport/session
    next();
});

app.use("/listings", listingRouter);
app.use("/listings/:id/reviews",reviewRouter);
app.use("/",userRouter);

app.use((err, req, res, next) => {
    if (res.headersSent) return next(err); // Prevent double response
    const { statusCode = 500, message = "Something went wrong" } = err;
    res.status(statusCode).render("error.ejs", { message });
});

app.get("/listings/reserve",(req,res) => {
    res.send("hello")
})
// app.js
app.get("/", (req, res) => {
  res.render("home"); // views/home.ejs render à¤¹à¥‹à¤—à¤¾
});



app.listen(8080, () => {
    console.log("Server is Started on port 8080");
});

